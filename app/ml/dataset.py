"""Небольшой встроенный датасет для обучения демо-модели токсичности.

В реальном проекте данные берутся из размеченного корпуса. Здесь — компактный
сбалансированный набор, чтобы pipeline обучался и проходил тесты без внешних
файлов.
"""

from __future__ import annotations

# 1 — токсично, 0 — нормально.
SAMPLES: list[tuple[str, int]] = [
    ("ты полный идиот и придурок", 1),
    ("какая же ты мразь", 1),
    ("заткнись тупой ублюдок", 1),
    ("ненавижу тебя, отвратительный человек", 1),
    ("ты дурак и ничтожество", 1),
    ("убирайся отсюда, мерзость", 1),
    ("что за тупость ты несёшь", 1),
    ("идиотское сообщение, отстой", 1),
    ("спасибо большое за помощь", 0),
    ("отличная работа, всем привет", 0),
    ("давайте обсудим проект завтра", 0),
    ("хорошего дня и хорошего настроения", 0),
    ("подскажите, как настроить бота", 0),
    ("согласен с вашим мнением", 0),
    ("рад видеть всех в чате", 0),
    ("отличная идея, поддерживаю", 0),
]


def load_dataset() -> tuple[list[str], list[int]]:
    """Вернуть тексты и метки для обучения."""
    texts = [text for text, _ in SAMPLES]
    labels = [label for _, label in SAMPLES]
    return texts, labels
